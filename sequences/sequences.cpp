#include <iostream>
using namespace std;

int main()
{
    // the backslash
    cout << "this // that" << endl;
    // tab
    cout << "a tab \t" << endl;
    // the newline
    cout << "a new line is here \n"
         << endl;
    // the double quote
    cout << "we are printing a double quote\"" << endl;
    // the single quote
    cout << "we are printing a double quote\'" << endl;
    // a bill
    cout << "\a" << endl;
}